function emojiStringToArray(str) {
  var split = str.split(/([\uD800-\uDBFF][\uDC00-\uDFFF])/);
  var arr = [];
  for (var i=0; i<split.length; i++) {
    var char = split[i];
    if (char !== "") {
      arr.push(char);
    }
  }
  return arr;
}

